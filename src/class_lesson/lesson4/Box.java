package class_lesson.lesson4;

public class Box {
    double width;
    double height;
    double depth;

    Box(){
        width = 0;
        height = 0;
        depth = 0;
    }

    Box(double width, double height, double depth){
        this.width = width;
        this.height = height;
        this.depth = depth;
    }

}
